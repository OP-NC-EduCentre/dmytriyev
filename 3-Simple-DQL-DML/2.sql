-- 2.1 Для однієї з таблиць створити команду отримання значень всіх колонок (явно 
-- перерахувати) у всіх рядках.
SELECT 
    car_number,
    brand,
    color,
    production_year,
    type,
    weight,
    owner,
    purchase_date
FROM Car;

-- CAR_NUMBER|BRAND |COLOR   |PRODUCTION_YEAR        |TYPE      |WEIGHT|OWNER|PURCHASE_DATE          |
-- ----------+------+--------+-----------------------+----------+------+-----+-----------------------+
